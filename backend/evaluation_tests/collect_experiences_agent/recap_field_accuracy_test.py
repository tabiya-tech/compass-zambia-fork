"""
Regression test for the recap embellishment bug.

PURPOSE:
    Verify that the final recap generated by the collect_experiences agent contains
    ONLY the five structured fields (title, work_type, company, location, dates)
    and does NOT include duties, responsibilities, or skills that were described
    during the conversation but are not stored in CollectedData.

BACKGROUND:
    The collect_experiences agent stores only five fields per experience:
      title, company, location, start_date, end_date (+ work_type).
    When generating the final recap it was instructed to "summarise all experiences
    clearly", which caused it to draw on conversation history and include duties
    and skills that exist only in the chat transcript — not in the structured store.

    The fix tightened the recap instruction to:
      "Present each experience using ONLY the fields stored in '#Collected Experience Data':
       title, company, location, start date, end date.
       Do NOT add duties, responsibilities, skills, or any details that are not explicitly
       stored in those fields."

WHAT THIS TEST MEASURES:
    The user explicitly describes duties for two experiences during collection:
      1. PAID: Data Entry Clerk at Zamtel (duties: entering data, filing forms)
      2. VOLUNTEERING: Survey Enumerator at Community Voice Zambia
         (duties: going door-to-door, asking questions, recording answers)

    After all work types are explored the agent generates a final recap.
    The RECAP_FIELD_ACCURACY evaluator scores whether the recap is clean
    (only structured fields) or dirty (embellished with duties/skills).

PASS CRITERIA:
    Score >= 80/100 — recap is clean, contains only the five structured fields.

FAIL CRITERIA (bug present):
    Score < 60 — recap includes duties or skills from the conversation history.
"""

import logging
import os
from typing import Coroutine, Callable, Awaitable

import pytest
from _pytest.logging import LogCaptureFixture

from app.conversation_memory.conversation_memory_manager import ConversationMemoryManager, \
    ConversationMemoryManagerState
from app.countries import Country
from app.i18n.translation_service import get_i18n_manager
from app.server_config import UNSUMMARIZED_WINDOW_SIZE, TO_BE_SUMMARIZED_WINDOW_SIZE
from common_libs.test_utilities import get_random_session_id
from common_libs.test_utilities.guard_caplog import guard_caplog, assert_log_error_warnings
from evaluation_tests.collect_experiences_agent.collect_experiences_executor import CollectExperiencesAgentExecutor, \
    CollectExperienceAgentGetConversationContextExecutor, CollectExperienceAgentIsFinished
from evaluation_tests.conversation_libs.conversation_test_function import conversation_test_function, \
    ConversationTestConfig, ScriptedUserEvaluationTestCase, ScriptedSimulatedUser, Evaluation
from evaluation_tests.conversation_libs.evaluators.evaluation_result import EvaluationType


@pytest.fixture(scope="function")
async def setup_collect_experiences_agent() -> tuple[
    ConversationMemoryManager,
    Callable[
        [LogCaptureFixture, ScriptedUserEvaluationTestCase, Country],
        Coroutine[None, None, None]
    ]
]:
    session_id = get_random_session_id()
    conversation_manager = ConversationMemoryManager(UNSUMMARIZED_WINDOW_SIZE, TO_BE_SUMMARIZED_WINDOW_SIZE)
    conversation_manager.set_state(state=ConversationMemoryManagerState(session_id=session_id))

    async def collect_experiences_exec(caplog: LogCaptureFixture, test_case: ScriptedUserEvaluationTestCase,
                                       country: Country):
        get_i18n_manager().set_locale(test_case.locale)
        output_folder = os.path.join(os.getcwd(),
                                     'test_output/collect_experiences_agent/recap_field_accuracy', test_case.name)
        execute_evaluated_agent = CollectExperiencesAgentExecutor(
            conversation_manager=conversation_manager,
            session_id=session_id,
            country_of_user=country
        )

        config = ConversationTestConfig(
            max_iterations=len(test_case.scripted_user),
            test_case=test_case,
            output_folder=output_folder,
            execute_evaluated_agent=execute_evaluated_agent,
            execute_simulated_user=ScriptedSimulatedUser(script=test_case.scripted_user),
            # Let the agent finish naturally — the recap only fires after all work types explored.
            is_finished=CollectExperienceAgentIsFinished(executor=execute_evaluated_agent),
            get_conversation_context=CollectExperienceAgentGetConversationContextExecutor(
                conversation_manager=conversation_manager)
        )

        with caplog.at_level(logging.DEBUG):
            guard_caplog(logger=execute_evaluated_agent._agent._logger, caplog=caplog)
            await conversation_test_function(config=config)
            assert_log_error_warnings(caplog=caplog,
                                      expect_errors_in_logs=False,
                                      expect_warnings_in_logs=True)

    return conversation_manager, collect_experiences_exec


@pytest.mark.asyncio
@pytest.mark.evaluation_test("gemini-2.5-flash-lite/")
@pytest.mark.repeat(3)
async def test_recap_contains_only_structured_fields(
        caplog: LogCaptureFixture,
        setup_collect_experiences_agent: Awaitable[tuple[
            ConversationMemoryManager,
            Callable[[LogCaptureFixture, ScriptedUserEvaluationTestCase, Country], Coroutine[None, None, None]]
        ]],
        setup_multi_locale_app_config):
    """
    Regression test: the final recap must contain ONLY the five structured fields.

    The user explicitly describes duties for both experiences during collection.
    On the old prompt ("summarise clearly") the agent would include those duties
    in the recap — scoring below 60.
    On the fixed prompt (constrained to stored fields only) the recap is clean
    — scoring >= 80.

    A consistent score >= 80 across 3 runs confirms the fix holds.
    """

    given_test_case = ScriptedUserEvaluationTestCase(
        name='recap_field_accuracy',
        simulated_user_prompt=(
            "Scripted user: Zambian user with two experiences — "
            "a paid data entry job at Zamtel where they entered data and filed forms, "
            "and an unpaid volunteering role at Community Voice Zambia where they went "
            "door-to-door asking questions and recording answers."
        ),
        scripted_user=[
            # --- Paid experience: Data Entry Clerk at Zamtel ---
            # Turn 1: agent asks if user has worked
            "yes",
            # Turn 2: agent asks about paid work
            "I worked as a Data Entry Clerk at Zamtel, a telecom company in Lusaka",
            # Turn 3: agent asks about dates
            "I was there from January 2021 to December 2022",
            # Turn 4: agent asks about location
            "Lusaka, Cairo Road branch",
            # Turn 5: agent asks about duties — DUTIES EXPLICITLY STATED HERE
            "My main duties were entering customer data into their billing system and filing forms",
            # Turn 6: any more paid jobs?
            "no, that was my only paid job",

            # --- Volunteering experience: Survey Enumerator at Community Voice Zambia ---
            # Turn 7: agent transitions to volunteering
            "yes I volunteered",
            # Turn 8: agent asks about role
            "I volunteered as a Survey Enumerator for Community Voice Zambia, a local NGO",
            # Turn 9: agent asks about dates
            "it was in 2020, for about three months",
            # Turn 10: agent asks about location
            "we worked in Kabwe and surrounding townships",
            # Turn 11: agent asks about duties — DUTIES EXPLICITLY STATED HERE
            "we went door to door asking people questions and recording their answers",
            # Turn 12: any more volunteering?
            "no that is the only volunteering I did",
        ],
        evaluations=[
            Evaluation(
                type=EvaluationType.RECAP_FIELD_ACCURACY,
                # Fixed code: recap contains only structured fields → score >= 80.
                # Buggy code: recap embellishes with duties from conversation → score < 60.
                expected=80
            )
        ]
    )

    conversation_manager, collect_experiences_exec = await setup_collect_experiences_agent
    await collect_experiences_exec(caplog, given_test_case, Country.ZAMBIA)
